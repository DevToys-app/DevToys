namespace DevToys.Api;

/// <summary>
/// Represents the style of a <see cref="IUILabel"/>.
/// </summary>
public enum UILabelStyle
{
    /// <summary>
    /// The caption style.
    /// </summary>
    Caption,

    /// <summary>
    /// The body style.
    /// </summary>
    Body,

    /// <summary>
    /// The strong body style.
    /// </summary>
    BodyStrong,

    /// <summary>
    /// The large body style.
    /// </summary>
    BodyLarge,

    /// <summary>
    /// The subtitle style.
    /// </summary>
    Subtitle,

    /// <summary>
    /// The title style.
    /// </summary>
    Title,

    /// <summary>
    /// The large title style.
    /// </summary>
    TitleLarge,

    /// <summary>
    /// The display style.
    /// </summary>
    Display
}
