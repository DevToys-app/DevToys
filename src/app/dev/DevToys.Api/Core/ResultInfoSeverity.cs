namespace DevToys.Api;

/// <summary>
/// Represents the severity of a result.
/// </summary>
public enum ResultInfoSeverity
{
    /// <summary>
    /// Indicates the result is a success.
    /// </summary>
    Success,

    /// <summary>
    /// Indicates the result is a warning.
    /// </summary>
    Warning,

    /// <summary>
    /// Indicates the result is an error.
    /// </summary>
    Error
}
